batch_size = 10
sfreq      = None  # Will be set from the first successfully loaded file
eeg_channels_ref = None  # Will store channel names for ERP plot in Phase 5


# ----------------------------
# TRAIN FILES - Batch Processing
# ----------------------------
# Processes train files only when MODE = "train".
# Instead of loading all files into RAM at once, we process in batches
# of 10, save each batch to Google Drive, then free the memory.
# This keeps RAM usage flat regardless of how many files there are.

if MODE == "done":
    print("\nAll files already processed. Set RUN_PHASE2 = True to run Phase 2-6 analysis.")
elif MODE == "train":
    print("\n--- Running TRAIN preprocessing ---")

    for batch_num, i in enumerate(range(0, len(train_files), batch_size)):
        batch = train_files[i:i + batch_size]
        batch_epochs_list = []
        batch_labels_list = []

        for file in batch:
            # --- Drive Disconnection Guard (Train) ---
            # Google Drive can silently drop its connection during long-running jobs.
            # If read_raw_edf raises OSError (Errno 107), it means Drive disconnected
            # mid-read. We retry up to 3 times, force-remounting Drive each time.
            # If all 3 attempts fail, we skip this file and move on rather than crashing.
            for attempt in range(3):
                try:
                    raw = mne.io.read_raw_edf(file, preload=True, verbose=False)
                    break  # File loaded successfully - exit the retry loop
                except OSError as e:
                    if attempt < 2:
                        print(f"  Warning: Drive disconnected on {os.path.basename(file)}, remounting and retrying... (attempt {attempt+1}/3)")
                        drive.mount('/content/drive', force_remount=True)
                    else:
                        print(f"  Failed to read {os.path.basename(file)} after 3 attempts, skipping.")
                        raw = None
                        break

            if raw is None:
                continue  # Skip this file entirely if all retries failed

            sfreq = raw.info['sfreq']

            # Step 1 - Apply Bandpass Filter
            # Keeps only frequencies where P300 signals exist (0.1-30 Hz)
            raw.filter(0.1, 30, verbose=False)

            # Step 2 - Timepoints of Stimulus
            # Auto threshold adapts to each file's voltage scaling.
            # We find the midpoint between min and max to binarize the signal,
            # then detect rising edges (0->1 transitions) as stimulus onsets.
            stimulus_channel = raw.copy().pick_channels(['StimulusBegin']).get_data()[0]
            threshold = (stimulus_channel.min() + stimulus_channel.max()) / 2
            above = (stimulus_channel > threshold).astype(int)
            event_indices = np.where(np.diff(above) == 1)[0] + 1
            print(f"  [{os.path.basename(file)}] Stimulus events detected: {len(event_indices)}")

            # Step 3 - Type of Stimulus (Target/Non-target)
            # Same auto threshold approach applied to the StimulusType channel
            # to reliably extract binary target (1) vs non-target (0) labels.
            stimulus_type = raw.copy().pick_channels(['StimulusType']).get_data()[0]
            threshold_type = (stimulus_type.min() + stimulus_type.max()) / 2
            labels = (stimulus_type[event_indices] > threshold_type).astype(int)
            print(f"  Target: {np.sum(labels == 1)}, Non-target: {np.sum(labels == 0)}")

            # Step 4 - EEG Channels & Data Extraction
            # Extract all 32 EEG electrode channels and slice 800ms windows
            # starting at each stimulus onset index.
            eeg_channels = [ch for ch in raw.ch_names if ch.startswith("EEG")]
            # Standardize to 16 channels across all studies.
            # Some studies recorded with 16 electrodes, others with 32.
            # We keep only the first 16 to ensure all epochs have matching shapes
            # so batches from different studies can be concatenated without errors.
            eeg_channels = eeg_channels[:16]
            eeg_channels_ref = np.array(eeg_channels)  # Save for ERP plot in Phase 5
            eeg_data = raw.copy().pick_channels(eeg_channels).get_data()
            epoch_length = int(0.8 * sfreq)

            epochs = []
            for idx in event_indices:
                start = idx
                end = idx + epoch_length
                if end <= eeg_data.shape[1]:
                    epochs.append(eeg_data[:, start:end])

            if len(epochs) == 0:
                print(f"  WARNING: No epochs extracted from {file}, skipping.")
                del raw, eeg_data
                gc.collect()
                continue

            epochs = np.array(epochs)

            # Step 5 - Baseline Correction
            # Subtract the mean of the first 100ms from each epoch to remove
            # DC offset and slow drifts, making P300 signals easier to detect.
            baseline_samples = int(0.1 * sfreq)
            baseline = np.mean(epochs[:, :, :baseline_samples], axis=2, keepdims=True)
            epochs = epochs - baseline

            batch_epochs_list.append(epochs)
            batch_labels_list.append(labels[:len(epochs)])  # Align labels to extracted epochs

            # Free per-file memory immediately to keep RAM usage low
            del raw, eeg_data, epochs
            gc.collect()

        if len(batch_epochs_list) == 0:
            print(f"Batch {batch_num} had no valid epochs, skipping save.")
            continue

        # Save batch to Google Drive and free RAM.
        # Filename includes CHUNK_START so batches from different runs
        # are uniquely named and never overwrite each other.
        batch_epochs = np.concatenate(batch_epochs_list)
        batch_labels = np.concatenate(batch_labels_list)
        np.save(SAVE_PATH + f"train_epochs_chunk{CHUNK_START}_batch{batch_num}.npy", batch_epochs)
        np.save(SAVE_PATH + f"train_labels_chunk{CHUNK_START}_batch{batch_num}.npy", batch_labels)
        print(f"Saved: Train chunk {CHUNK_START} batch {batch_num} - shape: {batch_epochs.shape}")

        del batch_epochs, batch_labels, batch_epochs_list, batch_labels_list
        gc.collect()

    # Save channel names for later use in Phase 5 ERP plot
    if eeg_channels_ref is not None:
        np.save(SAVE_PATH + "eeg_channels_ref.npy", eeg_channels_ref)
        np.save(SAVE_PATH + "sfreq_ref.npy", np.array([sfreq]))

    print("\nAll train batches for this chunk processed and saved!")
    print("Next step: rerun with MODE = 'test' and the same CHUNK_START/CHUNK_END.")


# ----------------------------
