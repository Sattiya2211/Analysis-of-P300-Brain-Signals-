    from collections import defaultdict

    def load_epochs_from_files(file_list, sfreq_ref):
        """
        Re-reads EDF files, applies bandpass filter, detects stimulus events,
        extracts 800ms epochs and applies baseline correction.
        Returns (epochs array, labels array) or (None, None) if no valid epochs found.
        """
        ep_list, lb_list = [], []
        epoch_len = int(0.8 * sfreq_ref)

        for f in file_list:
            try:
                raw = mne.io.read_raw_edf(f, preload=True, verbose=False)
            except OSError:
                print(f"  Skipping unreadable file: {os.path.basename(f)}")
                continue

            raw.filter(0.1, 30, verbose=False)

            # Detect stimulus onsets using auto threshold
            sc  = raw.copy().pick_channels(['StimulusBegin']).get_data()[0]
            thr = (sc.min() + sc.max()) / 2
            ei  = np.where(np.diff((sc > thr).astype(int)) == 1)[0] + 1

            # Get target/non-target labels using auto threshold
            st  = raw.copy().pick_channels(['StimulusType']).get_data()[0]
            lbs = (st[ei] > (st.min() + st.max()) / 2).astype(int)

            # Extract EEG epochs, keep only those that fit within the recording
            chs  = np.array([ch for ch in raw.ch_names if ch.startswith("EEG")])
            eeg  = raw.copy().pick_channels(chs).get_data()

            valid = (ei + epoch_len) <= eeg.shape[1]
            ei    = ei[valid]
            lbs   = lbs[valid]
            if len(ei) == 0:
                continue

            eps = np.array([eeg[:, i:i + epoch_len] for i in ei])
            bl  = np.mean(eps[:, :, :int(0.1 * sfreq_ref)], axis=2, keepdims=True)
            eps -= bl

            ep_list.append(eps)
            lb_list.append(lbs)

        if not ep_list:
            return None, None
        return np.concatenate(ep_list), np.concatenate(lb_list)


    def epochs_to_features(epochs, sfreq_ref):
        """
        Extracts 96 features (mean, peak, latency x 32 channels)
        from the P300 time window (250-500ms).
        """
        s, e = int(0.25 * sfreq_ref), int(0.50 * sfreq_ref)
        return np.concatenate([
            epochs[:, :, s:e].mean(axis=2),
            epochs[:, :, s:e].max(axis=2),
            epochs[:, :, s:e].argmax(axis=2)
        ], axis=1)


